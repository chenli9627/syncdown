ai_impl = """## 5.10 AI 多模型辅助功能实现

AI 辅助功能由前端选区气泡、服务端代理接口和候选结果回填三部分组成。用户在编辑器中选中文本后，前端记录选区内容和操作类型，并向 /api/ai/action 发送请求。请求体包含 selectedText、action、locale、prompt 和 candidateCount 等字段。服务端读取 AI_API_KEY、AI_BASE_URL、AI_MODEL 和 AI_SECONDARY_MODEL 等环境变量，以 OpenAI 兼容协议请求模型服务。

系统在服务端完成模型列表去重和并发调用。当 candidateCount 为 1 时，仅调用默认模型；当 candidateCount 为 2 时，同时调用默认模型和备选模型，并将模型名称与生成结果一起返回前端。前端只负责展示候选结果，不接触模型密钥，从而降低密钥泄露风险。

AI 多模型候选结果生成的关键代码如下：

```ts
const models = Array.from(
  new Set([model, candidateCount === 2 ? secondaryModel : null].filter(Boolean)),
);

const candidates = (
  await Promise.all(
    models.map((candidateModel) =>
      requestAiCandidate({
        apiKey,
        baseUrl,
        locale: body.locale,
        model: candidateModel,
        payload: body,
      }),
    ),
  )
).filter((candidate) => candidate !== null);
```

上述代码表明，多模型对比并不是在浏览器中分别调用模型，而是在服务端统一完成。这样既能隐藏 API Key，也便于对超时、错误和空结果进行集中处理。当前端收到 candidates 后，会在 AI 结果面板中展示不同模型名称和生成内容。用户选择某个候选结果后，可以执行“替换选区”或“插入下方”。这两个操作都会调用编辑器命令修改 Tiptap 文档，并进一步由 Yjs 转换为 CRDT update，同步给同一文档房间内的其他协作者。

从工程边界看，AI 功能不直接修改数据库，也不绕过编辑器写入正文。它只生成候选文本，最终是否进入文档由用户确认。该实现方式能够兼顾 AI 大模型的智能生成能力与协作文档系统的数据安全性。"""


