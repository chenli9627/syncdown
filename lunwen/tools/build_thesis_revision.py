def replace_between(text: str, start: str, end: str, replacement: str) -> str:
    pattern = re.compile(re.escape(start) + r".*?" + re.escape(end), re.S)
    return pattern.sub(replacement.rstrip() + "\n\n" + end, text, count=1)


def replace_section(text: str, heading: str, next_heading_pattern: str, body: str) -> str:
    pattern = re.compile(rf"{re.escape(heading)}\n.*?(?=\n{next_heading_pattern})", re.S)
    if body.lstrip().startswith(heading):
        replacement = body.rstrip() + "\n"
    else:
        replacement = heading + "\n\n" + body.rstrip() + "\n"
    return pattern.sub(lambda _: replacement, text, count=1)


